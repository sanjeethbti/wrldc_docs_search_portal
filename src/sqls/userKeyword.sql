CREATE TABLE WRLDC_REST_SUBSET.USER_KEYWORD (
    kid NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    doc_id int NOT NULL,
    user_id int NOT NULL,
    keyWords_user VARCHAR2(250)  NULL,
    CONSTRAINT uk_pk PRIMARY KEY (doc_id, user_id),
    CONSTRAINT FK_Fileinfo FOREIGN KEY (doc_id)REFERENCES WRLDC_REST_SUBSET.FILE_INFO(id),
    CONSTRAINT FK_Userlogin FOREIGN KEY (user_id)REFERENCES WRLDC_REST_SUBSET.USER_LOGIN(USER_ID)
);